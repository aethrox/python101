# Operator and User Input

"""
+ Addition
- Subtraction
* Multiplication
/ Division
% Mod
** Exponentiation
// Floor division
"""

x = 15
y = 2

print("Addition", x + y)
print("Subtraction", x - y)
print("Multiplication", x * y)
print("Division", x / y)
print("Mod", x % y)
print("Exponentiation", x ** y)
print("Floor division", x // y)